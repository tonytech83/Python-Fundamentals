sequence_of_numbers = [int(x) for x in input().split()]

minimum_number = min(sequence_of_numbers)
maximum_number = max(sequence_of_numbers)
sum_of_all_numbers = sum(sequence_of_numbers)

print(f'The minimum number is {minimum_number}')
print(f'The maximum number is {maximum_number}')
print(f'The sum number is: {sum_of_all_numbers}')
